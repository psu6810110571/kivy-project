# ─── API หลัก ─────────────────────────────────────────────────────────────────
def get_questions(category: str, level: str = 'easy') -> list:
    """
    ดึงคำถามตามหมวดและความยาก
    คืนค่าเป็น list ของ dict พร้อม shuffle แล้ว
    """
    pool = QUESTIONS.get(category, QUESTIONS['general']).copy()
    random.shuffle(pool)

    count_map = {
        'easy':   5,
        'medium': 7,
        'hard':   10,
        'sudden': len(pool),
        'daily':  5,
    }
    n = count_map.get(level, 5)
    return pool[:n]